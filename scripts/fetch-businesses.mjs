// Refreshes lib/businesses.ts from Google Places API (New) — Nearby Search.
// Run weekly (see the "daxi-businesses-refresh" scheduled task) rather than
// per page view, so the app itself never calls a paid Places field at runtime.
//
// Usage: node --env-file=.env.local scripts/fetch-businesses.mjs

const OLD_STREET_CENTER = { lat: 24.884952, lng: 121.288238 };
const RADIUS_METERS = 3000;
const TOP_N_PER_CATEGORY = 20;

// One Nearby Search call per category, each capped at 20 results and ranked
// by popularity (proxy for review count, refined by our own sort below).
const CATEGORIES = [
  {
    tag: "美食",
    includedTypes: [
      "restaurant",
      "cafe",
      "bakery",
      "dessert_shop",
      "bar",
      "meal_takeaway",
    ],
  },
  {
    tag: "景點",
    includedTypes: [
      "tourist_attraction",
      "historical_place",
      "museum",
      "park",
    ],
  },
  {
    tag: "市集",
    includedTypes: [
      "market",
      "supermarket",
      "shopping_mall",
      "convenience_store",
      "store",
      "gift_shop",
    ],
  },
];

const EXCLUDED_NAMES = new Set(["大溪老街"]);

function haversineMeters(a, b) {
  const R = 6371000;
  const toRad = (deg) => (deg * Math.PI) / 180;
  const dLat = toRad(b.lat - a.lat);
  const dLng = toRad(b.lng - a.lng);
  const h =
    Math.sin(dLat / 2) ** 2 + Math.cos(toRad(a.lat)) * Math.cos(toRad(b.lat)) * Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(h), Math.sqrt(1 - h));
}

function formatDistance(meters) {
  if (meters < 1000) return `${Math.round(meters / 10) * 10}m`;
  return `${(meters / 1000).toFixed(1)}km`;
}

async function searchNearby(includedTypes, apiKey) {
  const res = await fetch("https://places.googleapis.com/v1/places:searchNearby", {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      "X-Goog-Api-Key": apiKey,
      "X-Goog-FieldMask":
        "places.id,places.displayName,places.formattedAddress,places.rating,places.userRatingCount,places.primaryType,places.location",
    },
    body: JSON.stringify({
      includedTypes,
      maxResultCount: TOP_N_PER_CATEGORY,
      rankPreference: "POPULARITY",
      languageCode: "zh-TW",
      locationRestriction: {
        circle: { center: { latitude: OLD_STREET_CENTER.lat, longitude: OLD_STREET_CENTER.lng }, radius: RADIUS_METERS },
      },
    }),
  });
  const data = await res.json();
  if (!res.ok) {
    throw new Error(`Places API error: ${data.error?.status ?? res.status} - ${data.error?.message ?? "unknown"}`);
  }
  return data.places ?? [];
}

function toBusiness(p, tag) {
  const lat = p.location.latitude;
  const lng = p.location.longitude;
  const distanceMeters = haversineMeters(OLD_STREET_CENTER, { lat, lng });
  return {
    placeId: p.id,
    name: p.displayName?.text ?? "未命名商家",
    address: p.formattedAddress ?? null,
    tag,
    googleType: p.primaryType ?? null,
    rating: p.rating ?? null,
    reviewCount: p.userRatingCount ?? 0,
    lat,
    lng,
    distanceMeters: Math.round(distanceMeters),
    distanceLabel: formatDistance(distanceMeters),
    mapsUrl: `https://www.google.com/maps/dir/?api=1&destination=${lat},${lng}&destination_place_id=${p.id}`,
  };
}

async function main() {
  const apiKey = process.env.GOOGLE_MAPS_API_KEY;
  if (!apiKey) {
    console.error("GOOGLE_MAPS_API_KEY is not set (expected in .env.local)");
    process.exit(1);
  }

  const businesses = [];
  // A place matching more than one category's included types (Google's type
  // overlap, e.g. a restaurant also tagged "store") is kept only under the
  // first category it matched — categories are checked in priority order —
  // so the same placeId never appears twice across the combined list.
  const usedIds = new Set();
  for (const { tag, includedTypes } of CATEGORIES) {
    const places = await searchNearby(includedTypes, apiKey);
    const seen = new Map();
    for (const p of places) seen.set(p.id, p);

    const categoryRows = Array.from(seen.values())
      .filter((p) => !EXCLUDED_NAMES.has(p.displayName?.text ?? ""))
      .filter((p) => !usedIds.has(p.id))
      .map((p) => toBusiness(p, tag))
      .sort((a, b) => b.reviewCount - a.reviewCount)
      .slice(0, TOP_N_PER_CATEGORY);

    for (const row of categoryRows) usedIds.add(row.placeId);
    businesses.push(...categoryRows);
  }

  const generatedAt = new Date().toISOString();

  const fileContents = `// AUTO-GENERATED by scripts/fetch-businesses.mjs — do not hand-edit.
// Refreshed weekly (see the "daxi-businesses-refresh" scheduled task) from
// Google Places API (New), Nearby Search, top ${TOP_N_PER_CATEGORY} by review
// count per category within ${RADIUS_METERS}m of Daxi Old Street. Regenerate with:
//   node --env-file=.env.local scripts/fetch-businesses.mjs

export type BusinessTag = "美食" | "景點" | "市集";

export type Business = {
  placeId: string;
  name: string;
  address: string | null;
  tag: BusinessTag;
  googleType: string | null;
  rating: number | null;
  reviewCount: number;
  lat: number;
  lng: number;
  distanceMeters: number;
  distanceLabel: string;
  mapsUrl: string;
};

export const businessesGeneratedAt = ${JSON.stringify(generatedAt)};

export const businesses: Business[] = ${JSON.stringify(businesses, null, 2)};
`;

  const outPath = new URL("../lib/businesses.ts", import.meta.url);
  await import("node:fs/promises").then((fs) => fs.writeFile(outPath, fileContents, "utf-8"));

  console.log(`Wrote ${businesses.length} businesses to lib/businesses.ts (generated ${generatedAt})`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
